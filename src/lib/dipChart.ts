export type DipResult = {
  /** result of the conversion (clamped into the charted range) */
  dip: number; // mm
  litres: number;
  /** fill rate of the bracketing segment, litres per mm */
  slope: number;
  /** bracketing chart rows used for the linear interpolation */
  lo: [number, number];
  hi: [number, number];
  /** set when the input fell outside the charted range and was clamped */
  oob: "below" | "above" | null;
};

/** Stock (litres) -> dip height (mm), linear interpolation between chart rows. */
export function stockToDip(tank: Tank, litres: number): DipResult {
  const r = DIP_CHARTS[tank].rows;
  const first = r[0];
  const last = r[r.length - 1];
  if (litres <= first[1]) {
    const a = first, b = r[1];
    return {
      dip: first[0], litres: first[1],
      slope: (b[1] - a[1]) / (b[0] - a[0]),
      lo: a, hi: b, oob: litres < first[1] ? "below" : null,
    };
  }
  if (litres >= last[1]) {
    const a = r[r.length - 2], b = last;
    return {
      dip: last[0], litres: last[1],
      slope: (b[1] - a[1]) / (b[0] - a[0]),
      lo: a, hi: b, oob: litres > last[1] ? "above" : null,
    };
  }
  let i = 1;
  while (r[i][1] < litres) i++;
  const a = r[i - 1], b = r[i];
  const t = (litres - a[1]) / (b[1] - a[1]);
  return {
    dip: a[0] + t * (b[0] - a[0]), litres,
    slope: (b[1] - a[1]) / (b[0] - a[0]),
    lo: a, hi: b, oob: null,
  };
}

/** Dip height (mm) -> stock (litres), linear interpolation between chart rows. */
export function dipToStock(tank: Tank, dip: number): DipResult {
  const r = DIP_CHARTS[tank].rows;
  const first = r[0];
  const last = r[r.length - 1];
  if (dip <= first[0]) {
    const a = first, b = r[1];
    return {
      dip: first[0], litres: first[1],
      slope: (b[1] - a[1]) / (b[0] - a[0]),
      lo: a, hi: b, oob: dip < first[0] ? "below" : null,
    };
  }
  if (dip >= last[0]) {
    const a = r[r.length - 2], b = last;
    return {
      dip: last[0], litres: last[1],
      slope: (b[1] - a[1]) / (b[0] - a[0]),
      lo: a, hi: b, oob: dip > last[0] ? "above" : null,
    };
  }
  let i = 1;
  while (r[i][0] < dip) i++;
  const a = r[i - 1], b = r[i];
  const t = (dip - a[0]) / (b[0] - a[0]);
  return {
    dip, litres: a[1] + t * (b[1] - a[1]),
    slope: (b[1] - a[1]) / (b[0] - a[0]),
    lo: a, hi: b, oob: null,
  };
}
